#include "3-calc.h"

/**
 * op_add - adds integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int, sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int, difference.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int, product.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int, quotient.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int, remainder.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
